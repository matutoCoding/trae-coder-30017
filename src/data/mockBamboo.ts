export const getQualityText = (quality: string): string => {
  const map: Record<string, string> = {
    excellent: '特选',
    good: '优良',
    normal: '合格',
    poor: '不合格',
  }
  return map[quality] || quality
}

export const getStatusText = (status: string): string => {
  const map: Record<string, string> = {
    available: '可用',
    used: '已使用',
    reserved: '已预留',
  }
  return map[status] || status
}
